ON pedido_personalizado(fecha_entrega);

-- 11. RESEÑA 
CREATE TABLE resena (
    id_resena NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_producto NUMBER NOT NULL,
    id_usuario NUMBER NOT NULL,
    id_pedido NUMBER,
    calificacion NUMBER NOT NULL CHECK(calificacion BETWEEN 1 AND 5),
    comentario CLOB NOT NULL,
    aprobada NUMBER(1) DEFAULT 1 NOT NULL,
    fecha_creacion TIMESTAMP DEFAULT SYSTIMESTAMP,
    fecha_modificacion TIMESTAMP DEFAULT SYSTIMESTAMP,

    CONSTRAINT fk_resena_producto
    FOREIGN KEY(id_producto) 
    REFERENCES producto(id_producto) ON DELETE CASCADE,

    CONSTRAINT fk_resena_usuario
    FOREIGN KEY(id_usuario) 
    REFERENCES usuario(id_usuario) ON DELETE CASCADE,

    CONSTRAINT fk_resena_pedido
    FOREIGN KEY(id_pedido) 
    REFERENCES pedido(id_pedido) ON DELETE SET NULL,

    CONSTRAINT uq_resena_usuario_producto 
    UNIQUE(id_usuario, id_producto)
);

-- Índices 
CREATE INDEX ix_resena_producto 
ON resena(id_producto, aprobada);

CREATE INDEX ix_resena_usuario 
ON resena(id_usuario);

CREATE INDEX ix_resena_fecha 
ON resena(fecha_creacion);

SELECT 
    p.nombre AS producto,
    u.nombre AS cliente,
    r.calificacion,
    SUBSTR(r.comentario, 1, 50) AS comentario,
    TO_CHAR(r.fecha_creacion, 'DD/MM/YYYY') AS fecha
FROM resena r
JOIN producto p ON r.id_producto = p.id_producto
JOIN usuario u ON r.id_usuario = u.id_usuario
ORDER BY r.fecha_creacion DESC
FETCH FIRST 5 ROWS ONLY;
