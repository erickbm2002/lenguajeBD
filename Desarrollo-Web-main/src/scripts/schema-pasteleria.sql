    FOREIGN KEY (id_producto) REFERENCES producto(id_producto)
);

CREATE INDEX ix_opcion_pedido_categoria_activo 
ON opcion_pedido(categoria, activo);

CREATE INDEX ix_pedido_personalizado_email 
ON pedido_personalizado(email);

