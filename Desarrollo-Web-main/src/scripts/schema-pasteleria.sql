--TABLAS:
--TABLA USUARIO
CREATE TABLE usuario (
    id_usuario NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR2(120) NOT NULL,
    correo VARCHAR2(190) NOT NULL UNIQUE,
    password_hash VARCHAR2(255) NOT NULL,
    activo NUMBER(1) DEFAULT 1 NOT NULL,
    fecha_creacion TIMESTAMP DEFAULT SYSTIMESTAMP,
    fecha_modificacion TIMESTAMP DEFAULT SYSTIMESTAMP,
    CONSTRAINT chk_correo CHECK (
        REGEXP_LIKE(correo,'^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$')
    )
);

CREATE INDEX ix_usuario_correo ON usuario(correo);

-- 5. TOKEN CONTRASEÑA
CREATE TABLE expiracion_token_contraseña (
    id_token NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_usuario NUMBER NOT NULL,
    codigo CHAR(6) NOT NULL,
    expira_en TIMESTAMP NOT NULL,
    usado NUMBER(1) DEFAULT 0 NOT NULL,
    fecha_creacion TIMESTAMP DEFAULT SYSTIMESTAMP,
    fecha_modificacion TIMESTAMP DEFAULT SYSTIMESTAMP,
    CONSTRAINT fk_token_usuario 
        FOREIGN KEY (id_usuario)
        REFERENCES usuario(id_usuario)
        ON DELETE CASCADE,
    CONSTRAINT chk_codigo 
        CHECK (REGEXP_LIKE(codigo, '^[0-9]{6}$'))
);

CREATE INDEX ix_token_usuario_codigo 
ON expiracion_token_contraseña(id_usuario, codigo, usado);

-- 6. PRODUCTO
CREATE TABLE producto (
    id_producto NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR2(140) NOT NULL UNIQUE,
    descripcion CLOB,
    conservacion CLOB,
    precio NUMBER(10,2) DEFAULT 0 NOT NULL,
    stock NUMBER DEFAULT 0 NOT NULL,
    activo NUMBER(1) DEFAULT 1 NOT NULL,
    fecha_creacion TIMESTAMP DEFAULT SYSTIMESTAMP,
    fecha_modificacion TIMESTAMP DEFAULT SYSTIMESTAMP,
    CONSTRAINT chk_precio CHECK (precio >= 0),
    CONSTRAINT chk_stock CHECK (stock >= 0)
);

CREATE TABLE producto_tamano (
    id_tamano NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_producto NUMBER NOT NULL,
    etiqueta VARCHAR2(60) NOT NULL,
    CONSTRAINT fk_tamano_producto FOREIGN KEY (id_producto)
    REFERENCES producto(id_producto) ON DELETE CASCADE,
    CONSTRAINT uq_tamano_producto UNIQUE (id_producto, etiqueta)
);

CREATE TABLE producto_sabor (
    id_sabor NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_producto NUMBER NOT NULL,
    etiqueta VARCHAR2(60) NOT NULL,
    CONSTRAINT fk_sabor_producto FOREIGN KEY (id_producto)
    REFERENCES producto(id_producto) ON DELETE CASCADE,
    CONSTRAINT uq_sabor_producto UNIQUE (id_producto, etiqueta)
);

CREATE TABLE producto_ingrediente (
    id_ingrediente NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_producto NUMBER NOT NULL,
    nombre VARCHAR2(120) NOT NULL,
    CONSTRAINT fk_ing_producto FOREIGN KEY (id_producto)
    REFERENCES producto(id_producto) ON DELETE CASCADE,
    CONSTRAINT uq_ing_producto UNIQUE (id_producto, nombre)
);

CREATE TABLE opcion_pedido (
    id_opcion NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    categoria VARCHAR2(50) NOT NULL,
    valor VARCHAR2(120) NOT NULL,
    descripcion CLOB,
    precio_adicional NUMBER(10,2) DEFAULT 0 NOT NULL,
    activo NUMBER(1) DEFAULT 1 NOT NULL,
    fecha_creacion TIMESTAMP DEFAULT SYSTIMESTAMP,
    fecha_modificacion TIMESTAMP DEFAULT SYSTIMESTAMP,
    CONSTRAINT uq_opcion UNIQUE(categoria, valor)
);

CREATE INDEX ix_opcion_categoria_activo 
ON opcion_pedido(categoria, activo);

CREATE TABLE pedido_personalizado (
    id_pedido NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    producto VARCHAR2(120) NOT NULL,
    sabor_bizcocho VARCHAR2(120) NOT NULL,
    sabor_relleno VARCHAR2(120) NOT NULL,
    tamano VARCHAR2(120) NOT NULL,
