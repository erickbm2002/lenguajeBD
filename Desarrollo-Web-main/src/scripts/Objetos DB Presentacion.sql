/* PACKAGE */


CREATE OR REPLACE PACKAGE PKG_RESENA 
AS
    PROCEDURE SP_INSERTAR_RESENA(VID_PRODUCTO NUMBER,
                                 VID_USUARIO NUMBER,
                                 VID_PEDIDO NUMBER,
                                 VCALIFICACION NUMBER,
                                 VCOMENTARIO CLOB);
                                 
    PROCEDURE SP_OBTENER_RESENA(VRESENA_ID NUMBER);

    PROCEDURE SP_ACTUALIZAR_RESENA(VRESENA_ID NUMBER,
                                   VCALIFICACION NUMBER,
                                   VCOMENTARIO CLOB,
                                   VAPROBADA NUMBER);

    PROCEDURE SP_DESAPROBAR_RESENA(VRESENA_ID NUMBER);

END PKG_RESENA;

--- BODY DEL PAQUETE
CREATE OR REPLACE PACKAGE BODY PKG_RESENA
AS
-------------- SP_INSERTAR_RESENA -------------- 
PROCEDURE SP_INSERTAR_RESENA(VID_PRODUCTO NUMBER,
                             VID_USUARIO NUMBER,
                             VID_PEDIDO NUMBER,
                             VCALIFICACION NUMBER,
                             VCOMENTARIO CLOB) AS
    BEGIN
    INSERT INTO resena (id_producto,
                        id_usuario,
                        id_pedido,
                        calificacion,
                        comentario,
                        aprobada,
                        fecha_creacion,
                        fecha_modificacion)
    VALUES (VID_PRODUCTO,
            VID_USUARIO,
            VID_PEDIDO,
            VCALIFICACION,
            VCOMENTARIO,
            1,
            SYSTIMESTAMP,
            SYSTIMESTAMP);
    COMMIT;
END SP_INSERTAR_RESENA;
-------------- SP_OBTENER_RESENA ---------------
PROCEDURE SP_OBTENER_RESENA(VRESENA_ID NUMBER)
AS
    VPRODUCTO resena.id_producto%TYPE;
    VUSUARIO resena.id_usuario%TYPE;
    VCALIFICACION resena.calificacion%TYPE;
    VAPROBADA resena.aprobada%TYPE;
BEGIN
    SELECT id_producto, id_usuario, calificacion, aprobada
    INTO VPRODUCTO, VUSUARIO, VCALIFICACION, VAPROBADA
    FROM resena
    WHERE id_resena = VRESENA_ID;
    DBMS_OUTPUT.PUT_LINE('ID RESENA: ' || VRESENA_ID);
    DBMS_OUTPUT.PUT_LINE('ID PRODUCTO: ' || VPRODUCTO);
    DBMS_OUTPUT.PUT_LINE('ID USUARIO: ' || VUSUARIO);
    DBMS_OUTPUT.PUT_LINE('CALIFICACION: ' || VCALIFICACION);
    DBMS_OUTPUT.PUT_LINE('APROBADA: ' || VAPROBADA);

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('Resena no existe');
END SP_OBTENER_RESENA;
-------------- SP_ACTUALIZAR_RESENA ------------ 
PROCEDURE SP_ACTUALIZAR_RESENA(VRESENA_ID NUMBER,
                                                 VCALIFICACION NUMBER,
                                                 VCOMENTARIO CLOB,
                                                 VAPROBADA NUMBER) AS
    BEGIN
   UPDATE resena
    SET calificacion = VCALIFICACION,
        comentario = VCOMENTARIO,
        aprobada = VAPROBADA,
        fecha_modificacion = SYSTIMESTAMP
    WHERE id_resena = VRESENA_ID;
    COMMIT;
END SP_ACTUALIZAR_RESENA;
-------------- SP_DESAPROBAR_RESENA ------------
PROCEDURE SP_DESAPROBAR_RESENA(VRESENA_ID NUMBER)
AS
BEGIN
    UPDATE resena
    SET aprobada = 0,
        fecha_modificacion = SYSTIMESTAMP
    WHERE id_resena = VRESENA_ID;
    COMMIT;
END SP_DESAPROBAR_RESENA;

END PKG_RESENA;




