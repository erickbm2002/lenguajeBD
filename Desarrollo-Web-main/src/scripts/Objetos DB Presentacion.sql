CREATE OR REPLACE FUNCTION FN_FECHA_ENTREGA(VID_PEDIDO IN NUMBER)
RETURN TIMESTAMP
AS
    VFECHA TIMESTAMP;
BEGIN
    SELECT fecha_entrega
    INTO VFECHA
