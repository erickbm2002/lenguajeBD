-- modifica el stock despues de la compra 

CREATE OR REPLACE TRIGGER TRG_ACTUALIZAR_STOCK
AFTER INSERT ON detalle_pedido
FOR EACH ROW
BEGIN
    UPDATE producto
    SET stock = stock - :NEW.cantidad,fecha_modificacion = SYSTIMESTAMP
    WHERE id_producto = :NEW.id_producto;
    IF SQL%ROWCOUNT = 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Producto no encontrado: ' || :NEW.id_producto);
    END IF;
END;



