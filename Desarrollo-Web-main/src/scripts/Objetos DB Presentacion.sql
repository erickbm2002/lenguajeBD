CREATE SEQUENCE SEQ_AUDITORIA_PEDIDO
START WITH 1
INCREMENT BY 1
NOCACHE
NOCYCLE;

CREATE TABLE AUDITORIA_PEDIDO (
    id_auditoria NUMBER PRIMARY KEY,
    id_usuario NUMBER,
    producto VARCHAR2(40),
    fecha_registro TIMESTAMP DEFAULT SYSTIMESTAMP,
    tipo_pedido VARCHAR2(20), 
    id_pedido_ref NUMBER
);

CREATE OR REPLACE TRIGGER TRG_AUDITORIA_PEDIDO
AFTER INSERT OR UPDATE ON pedido
FOR EACH ROW
BEGIN
    INSERT INTO auditoria_pedido (id_auditoria, id_usuario, producto, fecha_registro, tipo_pedido, id_pedido_ref)
    VALUES (seq_auditoria_pedido.NEXTVAL, :NEW.id_usuario, 'EN STOCK', SYSTIMESTAMP, 'NORMAL', :NEW.id_pedido);
END;

CREATE OR REPLACE TRIGGER TRG_AUDITORIA_PEDIDO_PERSONALIZADO
AFTER INSERT OR UPDATE ON pedido_personalizado
FOR EACH ROW
BEGIN
    INSERT INTO auditoria_pedido (id_auditoria, id_usuario, producto, fecha_registro, tipo_pedido, id_pedido_ref)
    VALUES (seq_auditoria_pedido.NEXTVAL, :NEW.id_usuario, :NEW.producto, SYSTIMESTAMP, 'PERSONALIZADO', :NEW.id_pedido);
END;

